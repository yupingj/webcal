package com.mletech.webapp.services;

public class Calculator {

	public int add(int a, int b) {
		return a + b;
	}

	public int minus(int a, int b) {
		return a - b;
	}

	public int times(int a, int b) {
		return a * b;
	}

	public int divid(int a, int b) {
		return a/b;
	}

}
